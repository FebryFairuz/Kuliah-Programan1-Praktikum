public class Praktikum{
	public static void main(String args[]){
	//inisialisasi variabel coba1 dengan tipe data boolean
		boolean coba1; //--> boolean hanya berisi true or false
		//memberikan nilai coba1 apakah 10 sama dengan 1 ?
		coba1 = (10 == 1);
		//mencetak keluaran dari hasil nilai coba1 yaitu true
			System.out.println(coba1);
		//memberikan nilai coba1 apakah 10 tidak sama dengan 1 ?	
		coba1 = (10 != 1);
		//mencetak keluaran dari hasil nilai coba1 yaitu false
			System.out.println(coba1);
	}
}